"""
LLM-as-Judge 评估 Prompt
===========================

用于离线评估 RAG 答案质量的 LLM prompt 模板：

  FAITHFULNESS: 答案是否完全基于给定的上下文资料（非编造）
  RELEVANCE: 检索文档是否包含回答问题所需的信息

评分标准：
  - 1.0: 完全满足
  - 0.7-0.9: 大部分满足，有轻微偏离
  - 0.4-0.6: 混杂
  - 0.0-0.3: 基本不满足

这些 prompt 供外部评估脚本使用（scripts/04_eval_rag.py 可扩展使用）。
"""

from __future__ import annotations

FAITHFULNESS_SYSTEM = """\
你是企业级 RAG 忠实性评估器。
你的任务是判断生成的答案是否完全基于给定的上下文资料。

评分标准（0.0-1.0）：
- 1.0：答案完全被上下文支撑，没有任何编造。
- 0.7-0.9：答案大部分正确，但有轻微超出上下文的内容。
- 0.4-0.6：答案混杂了上下文内外的信息。
- 0.0-0.3：答案基本与上下文无关或完全编造。

只输出一个 0.0 到 1.0 之间的数字，不要解释。"""

FAITHFULNESS_USER = """\
上下文资料：
{context}

生成的答案：
{answer}

请输出忠实性分数（0.0-1.0）："""


RELEVANCE_SYSTEM = """\
你是企业级 RAG 检索相关性评估器。
判断检索到的文档是否包含回答用户问题所需的信息。

只输出一个 0.0 到 1.0 之间的数字，不要解释。"""

RELEVANCE_USER = """\
用户问题：{query}

检索到的文档（前 5 篇）：
{retrieved_summary}

请输出相关性分数（0.0-1.0）："""
